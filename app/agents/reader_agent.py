# Узел 2: Извлечение типа и ID через LLM
async def extract_node(state: ReaderState) -> dict:
    """
        Извлечение типа документа и ID изделия через LLM
    """
    prompt = f"""
Ты эксперт в анализе документов. Извлеки из текста:
- Идентификационный номер.
- Тип документа: строго один из следующих по смыслу: "техническое описание", "результаты тестов", "состав материалов".

Текст документа:
{state["document_text"]}

Ответь строго в формате JSON: {{"doc_id": "значение", "doc_type": "тип"}}
"""
    messages = [{"role": "user", "content": prompt}]
    response = await gemma.chat(messages)
    print(response)
    try:
        data = json.loads(response["content"].replace("json", "").replace("```", ""))
        doc_id = data.get("doc_id", "unknown")
        doc_type = data.get("doc_type", "техническое описание")  # default
    except:
        doc_id = "unknown"
        doc_type = "техническое описание"
    print({"doc_id": doc_id, "doc_type": doc_type})
    return {"doc_id": doc_id, "doc_type": doc_type}

# Узел 3: Запись в Redis-очередь
async def write_node(state: ReaderState) -> dict:
    doc_data = {
        "text": state["document_text"],
        "doc_type": state["doc_type"],
        "doc_id": state["doc_id"]
    }
    print(state['document_text'])
    # Запись в очередь необработанных
    r.rpush("unprocessed_docs", json.dumps(doc_data))
    print(f"[reader] Добавлен документ {state['doc_id']} в очередь unprocessed_docs")

    return {"processed": True}

# Построение графа
builder = StateGraph(ReaderState)
builder.add_node("read", read_node)
builder.add_node("extract", extract_node)
builder.add_node("write", write_node)
builder.set_entry_point("read")
builder.add_edge("read", "extract")
builder.add_edge("extract", "write")

reader_graph = builder.compile()

# Запуск агента
async def run_reader():
    initial_state = {"document_text": None, "doc_type": None, "doc_id": None, "processed": False}
    result = await reader_graph.ainvoke(initial_state)
    print("[reader] Агент завершён:", result["processed"])

async def main():
    await run_reader()

if __name__ == "__main__":
    asyncio.run(main())
